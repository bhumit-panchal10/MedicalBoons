    <script>
        function getPincodes() {
            var stateId = $("#stateid").val();
            var cityId = $("#areacityId").val();

            if (stateId && cityId) {
                $.ajax({
                    url: "{{ route('get.pincodes') }}",
                    type: 'GET',
                    data: {
                        state_id: stateId,
                        city_id: cityId
                    },
                    success: function(data) {
                        //var pincodeOptions = "<option value='' disabled selected>Select Pincode</option>";
                        var pincodeOptions = "";
                        $.each(data, function(pinId, pincode) {
                            pincodeOptions += "<option value='" + pinId + "'>" + pincode + "</option>";
                        });
                        $("#pincode").html(pincodeOptions);
                    },
                    error: function(xhr) {
                        console.error("Error fetching pincodes:", xhr.responseText);
                    }
                });
            } else {
                $("#pincode").html("<option value='' disabled selected>Select Pincode</option>");
            }
        }

        // Call getPincodes() when the city dropdown is changed
        $("#areacityId").change(function() {
            getPincodes();
        });
    </script>


    <script>
        function getcity() {
            var state = $("#stateid").val();
            var url = "{{ route('area.state_city_mapping', ':state') }}";
            url = url.replace(":state", state);
            $.ajax({
                url: url,
                type: 'GET',
                data: {
                    state: state,
                },
                success: function(data) {
                    $("#areacityId").html('');
                    $("#areacityId").append(data);
                }
            });
        }
    </script>

